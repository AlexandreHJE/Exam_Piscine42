#include <unistd.h>

int		ft_max(int *tab, unsigned int length)
{
	int max;
	int i;

	i = 0;
	max = -2147483648;
	while (length > 0)
	{
		if (tab[i] > max)
			max = tab[i];
		length--;
		i++;
	}
	return (max);
}